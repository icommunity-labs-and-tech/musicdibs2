// Merge legal, FAQ and AI Studio translations into resources
const langs = ['es', 'en', 'pt-BR'] as const;
langs.forEach((lang) => {
  const key = lang === 'pt-BR' ? 'pt-BR' : lang;
  if (resources[key] && legalTranslations[lang]) {
    Object.assign(resources[key].translation, legalTranslations[lang]);
  }
  if (resources[key] && faqTranslations[lang]) {
    Object.assign(resources[key].translation, faqTranslations[lang]);
  }
});

// Merge AI Studio subpage translations (all 6 languages)
const allLangs = ['es', 'en', 'pt-BR', 'fr', 'it', 'de'] as const;
allLangs.forEach((lang) => {
  if (resources[lang] && aiStudioTranslations[lang]) {
    Object.assign(resources[lang].translation, aiStudioTranslations[lang]);
  }
});

allLangs.forEach((lang) => {
  const translation = resources[lang]?.translation as Record<string, any> | undefined;
  if (!translation) return;

  const aiStudioRoot = aiStudioRootTranslations[lang]?.aiStudio;
  if (aiStudioRoot) {
    translation.aiStudio = {
      ...(translation.aiStudio || {}),
      ...aiStudioRoot,
    };
  }

  const dashboardWidgets = dashboardWidgetTranslations[lang]?.dashboard;
  if (dashboardWidgets) {
    translation.dashboard = {
      ...(translation.dashboard || {}),
      ...dashboardWidgets,
    };
  }
});

// Safety fix: in some locales aiStudio was accidentally nested under privacy
allLangs.forEach((lang) => {
  const translation = resources[lang]?.translation as Record<string, any> | undefined;
  if (!translation) return;

  const misplacedAiStudio = translation?.privacy?.aiStudio;
  if (misplacedAiStudio && !translation.aiStudio) {
    translation.aiStudio = misplacedAiStudio;
    delete translation.privacy.aiStudio;
  }
});

i18n
  .use(LanguageDetector)
  .use(initReactI18next)
  .init({
    resources,
    lng: detectedLang,
    fallbackLng: 'es',
    supportedLngs: ['es', 'en', 'pt-BR', 'fr', 'it', 'de'],
    nonExplicitSupportedLngs: true,
    load: 'languageOnly',
    cleanCode: true,
    interpolation: { escapeValue: false },
    detection: {
      order: ['localStorage', 'navigator'],
      lookupLocalStorage: 'lang',
      caches: ['localStorage'],
    },
  });

i18n.on('languageChanged', (lng) => {
  const normalized = mapBrowserLang(lng);
  if (lng !== normalized) {
    void i18n.changeLanguage(normalized);
    return;
  }

  // Update <html lang> for accessibility and SEO
  if (typeof document !== 'undefined') {
    document.documentElement.lang = normalized === 'pt-BR' ? 'pt-BR' : normalized;
  }

  try {
    localStorage.setItem('lang', normalized);
  } catch {
    // Ignore storage errors (private mode, etc.)
  }
});

// Set initial <html lang>
if (typeof document !== 'undefined') {
  document.documentElement.lang = detectedLang === 'pt-BR' ? 'pt-BR' : detectedLang;
}

